// FIC: Coverage strategy contract — types for all coverage strategy operations. (EN)
// FIC: Contrato de estrategias de cobertura — tipos para todas las operaciones de cobertura. (ES)

/** Strategy kinds supported / Tipos de estrategia soportados */
// FIC: "secured_put" and "covered_call" added for Wheel strategy phase 1. (EN)
// FIC: "secured_put" y "covered_call" añadidos para la fase 1 de la estrategia Wheel. (ES)
export type CoverageStrategyKind =
  | "protective_put"
  | "married_put"
  | "collar_put"
  | "covered_straddle"
  | "secured_put"
  | "covered_call";

/** Alert severity levels / Niveles de severidad de alertas */
export type CoverageStrategyAlertSeverity = "info" | "warning" | "critical";

/** Single option leg in a coverage strategy / Pata de opción en una estrategia de cobertura */
export interface CoverageStrategyLeg {
  type: "call" | "put";
  position: "long" | "short";
  strike: number;
  premium: number;
  expiry: string; // ISO date string
  quantity?: number;
}

/** Input contract for a coverage strategy engine / Contrato de entrada para un engine de cobertura */
export interface CoverageStrategyContract {
  strategyId: string;
  kind: CoverageStrategyKind;
  ticker: string;
  shares: number;
  underlyingPrice: number;
  legs: CoverageStrategyLeg[];
  capital: number;
  riskTolerancePct: number; // 0–1, e.g. 0.05 = 5%
  requestedAt: string; // ISO timestamp
  // FIC: Real market params — set by analyze/simulate routes from live option chain. (EN)
  // FIC: Parámetros reales del mercado — asignados por las rutas desde la cadena de opciones en vivo. (ES)
  iv?: number;  // implied volatility (annualized), e.g. 0.32 = 32%
  dte?: number; // days to expiration of the primary leg
}

/** Single point on a payoff diagram / Punto en el diagrama de payoff */
export interface CoveragePayoffPoint {
  underlyingPrice: number;
  pnl: number;
}

/** Risk and cost metrics for a coverage strategy / Métricas de riesgo y costo de una estrategia de cobertura */
export interface CoverageRiskMetrics {
  riskProfile: "limited" | "unlimited";
  maxProtection: number;
  protectionFloorPrice: number;
  protectionCeilingPrice?: number;
  netPremium: number;
  netPremiumPerShare: number;
  costBenefitRatio: number;
  downsideRisk: number;
  upsideCap: number;
  breakEvenPrice: number;
  marginRequirement: number;
  exerciseRiskScore: number;
  volatilityStressLoss: number;
  stopLossPrice: number;
  stopLossLowPrice?: number; // collar lower band / Banda inferior del collar
  stopLossHighPrice?: number; // collar upper band / Banda superior del collar
}

/** Alert generated by a coverage engine / Alerta generada por un engine de cobertura */
export interface CoverageStrategyAlert {
  code: string;
  severity: CoverageStrategyAlertSeverity;
  message: string;
}

/** Full result emitted by a coverage strategy engine / Resultado completo emitido por un engine de cobertura */
export interface CoverageStrategyResult {
  strategyId: string;
  kind: CoverageStrategyKind;
  ticker: string;
  shares: number;
  underlyingPrice: number;
  legs: CoverageStrategyLeg[];
  riskMetrics: CoverageRiskMetrics;
  payoffPoints: CoveragePayoffPoint[];
  maxProfit: number; // may be Infinity for pure-protection strategies
  maxLoss: number;
  alerts: CoverageStrategyAlert[];
  confidenceScore: number; // 0–1
  generatedAt: string; // ISO timestamp
}
